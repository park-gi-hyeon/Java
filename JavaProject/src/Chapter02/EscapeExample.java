package Chapter02;

public class EscapeExample {
	public static void main(String[] args) {
//		\t : 탭만큼 띄움
		System.out.println("번\t이\t직");
//		\n: 엔터(다음행)
		System.out.print("번\t이\t직\n");
		
		System.out.print("번\t이\t직");
		System.out.println("우리는 개발자 입니다.");
//		큰따옴표 출력시 백슬래시 필요
		System.out.println("우리는 \"개발자\"입니다");
//		백슬래시 출력시 백슬래시 필요
		System.out.println("봄\\여름\\가을\\겨울");
		//번호이름직업이라는뜻 ㅎ
		
		
		
		
		
		
		
		
		
	}
}
